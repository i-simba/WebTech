				<p>&emsp; From a very early age, around 6 or 7, I distincly remember being drawn to drums. It's hard to explain, but it came very natural to me. My family didn't have the money to buy me a drum set, so for the longest time I used anything I could as 'drums'. The drum sticks I used were nothing but tree branches, and the drums themselves were tin cans.
				My dad was finally able to buy me a drum set when I was 15 years old. I wanted to play it all day and all night, but that simply isn't possible due to how inherently loud drums are. It was hard for me to find any time to practice. The only time I found to practice is the small 2 hour window between me getting home from school and my parents getting home from work. After a while, I got a job and saved up for an electronic drum set. It was great and allowed me to practice longer because it was quiet.<br>
				&emsp; The electronic drum set didn't last, however. It had foam triggers with regular drum heads rather than mesh heads. Over time, the heads and foam got more and more damaged. The damage caused the drums not to trigger as well and leaving essentially no rebound, making it hard to play and practice fundamentals.<br>
				&emsp; Finally, after I graduated high school, I sold my electronic drum set and bought a new acoustic drum set. It was a 4 piece Mapex kit. I also bought Sabian hi-hats, crash cymbals, and ride cymbal. This was the last kit I ever bought. After playing on this kit for a while, I bought microphones and an xlr splitter to record myself playing the drums. I did this for a while, recording differnt songs and genres. Sadly, I haven't played in a while as my drum set is at my parent's house and I live in an apartment. I would receive endless noise complaints had I brought my drum set here (lol).</p>
